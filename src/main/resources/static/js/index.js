function showTextToGraph() {

    document.getElementById('text-to-graph').style.display = 'block';
    document.getElementById('graph-to-text').style.display = 'none';
}

function showGraphToText() {

    document.getElementById('text-to-graph').style.display = 'none';
    document.getElementById('graph-to-text').style.display = 'block';
}
